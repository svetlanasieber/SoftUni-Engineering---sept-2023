# Да се напише програма, която чете цяло число,
# въведено от потребителя, и печата дали е четно или нечетно.
# Ако е четно отпечатайте "even", ако е нечетно "odd".


number = int(input())

# четно : "even"
# нечетно: "odd"
# или нещо е вярно или е грешно

if number % 2 == 0:
    print('even')
else: # number % 2 != 0
    print('odd')